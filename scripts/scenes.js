// 當前場景索引
window.currentSceneIndex = 0;

// 開始第一章
function startChapter1() {
    window.currentSceneIndex = 0;
    window.currentChapter = chapter1;
    showCurrentScene();
}

// 開始第二章
function startChapter2() {
    window.currentSceneIndex = 0;
    window.currentChapter = chapter2;
    showCurrentScene();
}

// 開始第三章
function startChapter3() {
    window.currentSceneIndex = 0;
    window.currentChapter = chapter3;
    showCurrentScene();
}

// 開始第四章
function startChapter4() {
    window.currentSceneIndex = 0;
    window.currentChapter = chapter4;
    showCurrentScene();
}

// 開始第五章
function startChapter5() {
    window.currentSceneIndex = 0;
    window.currentChapter = chapter5;
    showCurrentScene();
}

// 顯示當前場景
async function showCurrentScene() {
    if (!window.currentChapter) {
        window.currentChapter = chapter1;
    }
    
    if (window.currentSceneIndex >= 0 && window.currentSceneIndex < window.currentChapter.scenes.length) {
        const currentScene = window.currentChapter.scenes[window.currentSceneIndex];
        await window.gameCore.updateScene(currentScene);
    }
}

// 進入下一個場景
function nextScene() {
    if (window.currentChapter && window.currentSceneIndex < window.currentChapter.scenes.length - 1) {
        window.currentSceneIndex++;
        showCurrentScene();
    } else if (window.currentChapter === chapter1) {
        // 第一章結束，開始第二章
        startChapter2();
    } else if (window.currentChapter === chapter2) {
        // 第二章結束，開始第三章
        startChapter3();
    } else if (window.currentChapter === chapter3) {
        // 第三章結束，開始第四章
        startChapter4();
    } else if (window.currentChapter === chapter4) {
        // 第四章結束，開始第五章
        startChapter5();
    }
}

// 跳過當前章節
async function skipChapter() {
    if (window.currentChapter === chapter1) {
        if (confirm('確定要跳過第一章嗎？將直接進入第二章。')) {
            startChapter2();
        }
    } else if (window.currentChapter === chapter2) {
        if (confirm('確定要跳過第二章嗎？將直接進入第三章。')) {
            startChapter3();
        }
    } else if (window.currentChapter === chapter3) {
        if (confirm('確定要跳過第三章嗎？將直接進入第四章。')) {
            startChapter4();
        }
    } else if (window.currentChapter === chapter4) {
        if (confirm('確定要跳過第四章嗎？將直接進入第五章。')) {
            startChapter5();
        }
    } else if (window.currentChapter === chapter5) {
        if (confirm('確定要跳過第五章嗎？這是最後一章了。')) {
            console.log('遊戲結束');
        }
    }
}

// 導出函數供其他模組使用
window.startChapter1 = startChapter1;
